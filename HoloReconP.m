classdef HoloReconP < hgsetget
    % Function stores all the parameters necessary for hologram
    % reconstruction, such as filter type, size, what signals to
    % reconstruct, etc.  
    %
    % Designed to work with FFTReconMulti.m
    
properties (SetAccess = protected) %write access limited
    % Setting protected properties only seems to cause me lots of headaches
end

properties
    reconSize % size to reconstruct hologram to
    holoSize % size of the original hologram
    sbPos % sideband maxima position
    
    % Processing flags
    keepRaw = false % whether to store the original hologram or not
    keepUnreg = true; % true keeps the unregistered data
    doSide = true % reconstruct sideband
    doCenter = true % reconstruct centerband. 
    doVismap = true % reconstruct visibility map.
    doFresnelFilt = false % filter removal of Fresnel fringes for 1 biprism machines
    doMedFilt = false % Median filter image to remove x-ray zingers and such
    medFiltSigma = 2.5;
    doBandWidth = false % Find bandwidth of the centerband and sideband
    doHoloStats = true % Calculate mean dose and visibility and such
    doCums = true;
    baseChoice = 'First'; %[Middle,First,Last,Custom] Defines which holo in stack is used as the base holo that all others are compared with
    
    % Detector properties
    gainRef; % Only set if you acquired holograms without GATANs reference system
    darkRef; % Only set if you acquired holograms without GATANs reference system
    % Ideally we would pull these from the dm3struct
    gain = 0.559; % gain of the detector e-/DN, default is for 300 keV on upper USC
    ps_holo = 0.0; % pixel size of the original hologram
    ps_recon; % pixel size of reconstruction
    ps_inv; % inverse pixel size in Fourier space
    dt; % mean time in-between exposures 
    q_c % carrier frequency (in pix^-1, multiply by ps_inv to get nm^-1)
    CB_ref % mean dose in reference vacuum
    
    % Fourier filter properties
    a = 0.5;
    filtName = 'hann'
    apodizationName = 'none';
    
    % Vismap properties
    vmApodName = 'hamming';
    vmFiltName = 'hann';
    vmCBref; % TO DO: Amplitude of reference centerband, for object visibility correction

    % Fresnel filter properties
    ffSideRadCut = 0.18; % Distance from autocorrelation to sideband to start filter at 
    ffCutHalfWidth = 3; % Width of the filter in pixels, the Fresnel fringes are quite wide!
    ffCutAngle = pi/8; % Hunk of frequency space to not sample from in arc
    ffSmoothSpan = 7; % Pixels to apply moving average filter to background
    ffFiltName = 'butterworth.12';
    ffSoftPad = 6; % When using a soft filter, you need to pad the filter a bit.  No harm done for hard aperture filters.
    
    % Alignment properties
    maxShiftPerFrame = 0; % the maximum shift allowed per frame ( 0 == NO LIMIT)
    xcWindow % FOUR ELEMENT array indicating [x1 y1 x2 y2] bounds to x-correlate too, interpolated from original hologram size to reconSize
    pmWindow % Similar to xcWindow, but used for the phase matching part
    xcOversample = 4.0; % Degree of oversampling to perform x-correlation (i.e. precise to 1./XCoversample pixels)
    xcMethod = 'masked' % Options: 'general', 'masked'
    xcMask % For use with, a binary mask the same size as the hologram.
end

methods
    % q_c and ps_recon need set methods.
    function [this] = set.xcWindow( this, pos )
        % To do: implement checking to make sure xcWindow is within bounds
        % of holoSize
        
        this.xcWindow = pos;
        if( isempty( this.pmWindow ) ) % Matlab complains but also doesn't handle get/set properly in this context!
            this.pmWindow = pos;
        end
    end
    
    function [this] = set.ps_holo( this, ps )
        this.ps_holo = ps;
        if( ~isempty( this.holoSize ) )
            if( this.holoSize(1) ~= this.holoSize(2) )
                disp( 'HoloReconP: Warning, not designed to work with non-square holograms at present' )
            end
            
            this.ps_inv = 1 ./ ( ps .* this.holoSize(1) );
            if( ~isempty( this.reconSize ) )
                % Set ps_recon
                this.ps_recon = this.ps_holo .* this.holoSize(1) ./ this.reconSize(1);
            end
        end
    end
    
        
end

end