classdef HoloClass < handle
    properties
        filename % who am I?
        number % Numbering in the ordering
        hp; % a HoloReconP object containing all the reconstruction parameters
        
        holo %raw hologram data from DM3Import
        side %reconstructed sideband
        center %centerband
        vismap % visibility map        
        regside; %registered image (shifted and cropped)
        regcenter; % registered centerband (shifted and cropped)
        regvismap; % registered visibility map (shifted and cropped)
        dmap; % density map
        
        cumSide; % cummulative sideband sum
        cumReg; % cummulative registered sideband sum
        phaseDiff  %partial derivative of phase images (used for alignment technique)
        oneDimProf %1-D profile, for FIB cross-sections

        offset %offset from base image (includes subpixel offset)
        subOffset %only subpixel offset
        pShift  %phase shift
                
        xc %cross-correlation image (not presently in use)
        xcEdge = [64 64]; % Default hp.xcWindow and pmWindow values
        
        dose; % The mean dose, from the actual hologram
        visibility; % The mean visibility within the hologram region, determined by histogram method
        bandSide; % The radius it takes the sideband to fall to 0.01 power
        bandCenter; % The radius it takes the centerband to fall to 0.01 power
    end
    methods
        %Constructor Function
        function this = HoloClass( in_filename, in_holoparam )
            if nargin > 0
                this.filename = in_filename;
            end
            if nargin > 1 
                this.hp = in_holoparam;
            else
                this.hp = HoloReconP();
            end
        end
        
        % Clone function, used for building new HoloStacks from existing
        % ones
        function new = copy(this)
            % Instantiate new object of the same class.
            new = feval(class(this));
            
            % Copy all non-hidden properties.
            p = properties(this);
            for i = 1:length(p)
                new.(p{i}) = this.(p{i});
            end
        end
        
        % Pass in a pre-processed hologram from somewhere else (typically used for simulation)  
        function build( this, holo_in )
            this.holo = holo_in;
        end
        
        %Reconstruct hologram
        function reconstruct( this )
            if( ~isempty(this.filename) ) % Experiment
                FFTReconMulti( this, this.hp );
            else % Simulation
                disp( 'FIX ME: HoloClass.reconstruct cannot handle simulated data' );
            end
        end
        
