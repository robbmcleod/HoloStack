        function [] = calcOffset1D( this, base, alignMethodName, alignMethodField, profileWidth )
            if( ~exist( 'alignMethodField' ) )
                alignMethodField = 'centerband';
            end
            
            if( isempty( this.hp.xcWindow ) )
                error( 'HoloClass.calcOffset1D: 1-D registration cannot continue without hp.xcWindow' )
            end
            
            xcRescale_x = this.hp.holoSize(1)./this.hp.reconSize(1);
            xcRescale_y = this.hp.holoSize(2)./this.hp.reconSize(2);
            reconXCWindow = [ ceil(this.hp.xcWindow(1)./xcRescale_x), ceil(this.hp.xcWindow(2)./xcRescale_y), ...
            floor(this.hp.xcWindow(3)./xcRescale_x), floor(this.hp.xcWindow(4)./xcRescale_y) ];
            
            switch lower(alignMethodName)
                case 'onedim'
                    % Absolute value, phase alignment would have to be
                    % handled seperately.
                    
                    if( profileWidth ==  1 )
                        % Use improfile with bicubic sampling
                        this.oneDimProf = improfile( abs(this.(alignMethodField)), [reconXCWindow(1), reconXCWindow(3)], ...
                            [reconXCWindow(2) reconXCWindow(4)], 'bicubic' );
                        % Oversample to increase precision
                        this.oneDimProf = interp1( this.oneDimProf, 1:(1/this.hp.xcOversample):length(this.oneDimProf), 'pchip' );
                        
                    elseif( profileWidth > 1 )
                        % Use profile_int, _might_ have some artifacts
                        this.oneDimProf = profile_int( abs(this.(alignMethodField)), [reconXCWindow(1), reconXCWindow(3)], ...
                            [reconXCWindow(2) reconXCWindow(4)], profileWidth );
                        % Oversample to increase precision
                        this.oneDimProf = interp1( this.oneDimProf, 1:(1/this.hp.xcOversample):length(this.oneDimProf), 'pchip' );
                    end
                otherwise
                    error('HoloClass.calcOffset1D: Unknown alignMethodName');
            end
            
            if( isempty( base.oneDimProf ) )
                if( isequal( this, base ) )
                    % No problem
                else
                    error( 'HoloClass.calcOffset1D: No base 1-D profile found');
                end
            end
                
            base1D = base.oneDimProf;
            tmplt1D = this.oneDimProf;
            
            xcAmp = normxcorr2_general( abs(base1D), abs(tmplt1D), length(base1D)./2 ); 
            
            xcLength = length( xcAmp );
            [~, xc_maxpos] = max( xcAmp );
            
%             this.offset = -((xcLength+1)./2 - xc_maxpos)./this.XCoversample;
            % Not currently oversampling the 2-D method, but it would be
            % pretty easy using interp1 with bicubic 
            % NOT NEGATIVE
            xcShift = ((xcLength+1)./2 - xc_maxpos)./this.hp.xcOversample;
            
%             figure; movegui;
%             plot( 1:numel(base1D), abs(base1D), 'r', 1:numel(tmplt1D), abs(tmplt1D), 'b', ...
%                 1:numel(xcAmp), xcAmp, 'k' );
%             legend( 'Base', 'Template', 'xcAmp' );
%             title( [ 'xcShift = ', num2str(xcShift) ] );
            
            % Now we need to use the coordinates of hp.xcWindow to calculate
            % the offset in terms of x and y
            theta = atan2( this.hp.xcWindow(4) - this.hp.xcWindow(2), this.hp.xcWindow(3) - this.hp.xcWindow(4) );
            this.offset(1) = sin(theta).*xcShift;
            this.offset(2) = cos(theta).*xcShift;
        end

        % Shift and crop the hologram
        % (in Mosiac-style we extend the size of the hologram but that is
        % not implemented here)
        function shift( this, fixOffset, regSize)
            %shift image and crop. Results stored in "reg"
            %create registered image: this.reg
            if( ~isempty( this.side ) )
                this.regside = holoShiftAndCrop( this.side, this.offset - fixOffset, regSize );
            end
            if( ~isempty( this.center ) )
                this.regcenter= abs( holoShiftAndCrop( this.center, this.offset - fixOffset, regSize ) );
            end
            if( ~isempty( this.vismap ) )
                this.regvismap = abs( holoShiftAndCrop( this.vismap, this.offset - fixOffset, regSize ) );
            end
        end
        
        % Apply a phase shift to match a base image.  Generally applied to
        % 'regside' 
        function [phiShift] = shiftPhase( this, base, fieldName )
            % Restrict to the cross-correlation window, which we need to
            % pick to 
            base = base.(fieldName);
            tmplt = this.(fieldName);
            
            if( strcmp( fieldName, 'regside' ) )
                if( isempty( this.hp.pmWindow ) )
                    disp( 'HoloClass.shiftPhase: No hp.pmWindow provided, using default xcEdge' )
                    this.hp.pmWindow = [ this.xcEdge+1, this.hp.holoSize - this.xcEdge ];
                end
                % Apply hp.hp.xcWindow
                pmWindowTemp = this.hp.pmWindow;
                % Generally we're operating on reg, but the hp.xcWindow should be
                % shrunk appropriately to deal with the difference in size
                % between this.sideband and this.reg
                xcRescale_x = this.hp.holoSize(1)./this.hp.reconSize(1);
                xcRescale_y = this.hp.holoSize(2)./this.hp.reconSize(2);
                % First rescale
                reconpmWindow = [ ceil(pmWindowTemp(1)./xcRescale_x), ceil(pmWindowTemp(2)./xcRescale_y), ...
                floor(pmWindowTemp(3)./xcRescale_x), floor(pmWindowTemp(4)./xcRescale_y) ];

                % Then remove the offset shift
                if( reconpmWindow(1) <= 0 )
                    disp( ['reconpmWindow(1) clipping: ', num2str(reconpmWindow(1)) ] );
                    reconpmWindow(1) = 1;
                end
                if( reconpmWindow(2) <= 0 )
                    disp( ['reconpmWindow(2) clipping: ', num2str(reconpmWindow(2)) ] );
                    reconpmWindow(2) = 1;
                end
                if( reconpmWindow(3) > size( tmplt, 2 ) )
                    disp( ['reconpmWindow(3) clipping: ', num2str(reconpmWindow(3)) ] );
                    reconpmWindow(3) = size( tmplt, 2 );
                end
                if( reconpmWindow(4) > size( tmplt, 1 ) )
                    disp( [ 'reconpmWindow(4) clipping: ', num2str(reconpmWindow(4)) ] );
                    reconpmWindow(4) = size( tmplt, 1 );
                end

            elseif( strcmp(fieldName, 'side') )
                
                 if( isempty( this.hp.pmWindow ) )
                    disp( 'HoloClass.shiftPhase: No hp.pmWindow provided, using default xcEdge' )
                    this.hp.pmWindow = [ this.xcEdge+1, this.hp.holoSize - this.xcEdge ];
                end
                % Apply hp.xcWindow
                pmWindowTemp = this.hp.pmWindow;
                % Generally we're operating on reg, but the hp.xcWindow should be
                % shrunk appropriately to deal with the difference in size
                % between this.sideband and this.reg
                xcRescale_x = this.hp.holoSize(1)./this.hp.reconSize(1);
                xcRescale_y = this.hp.holoSize(2)./this.hp.reconSize(2);
                % First rescale
                reconpmWindow = [ ceil(pmWindowTemp(1)./xcRescale_x), ceil(pmWindowTemp(2)./xcRescale_y), ...
                floor(pmWindowTemp(3)./xcRescale_x), floor(pmWindowTemp(4)./xcRescale_y) ];
            end
            
            tmplt = tmplt( reconpmWindow(2):reconpmWindow(4), reconpmWindow(1):reconpmWindow(3) );
            base = base( reconpmWindow(2):reconpmWindow(4), reconpmWindow(1):reconpmWindow(3) );
            
            % Trick from DFTregistration
            fftXC = fft2(base) .* conj(fft2(tmplt));
            % This is a bit of a bug, we always want to go for the
            % zero-frequency pixel.  I guess I don't really need the
            % fftshift anymore either, could just pick pixel (1,1)
            [maxVal,maxLoc] = max2(fftXC);
            
            phiShift = angle(maxVal);
            this.pShift = phiShift;

            shiftedHoloFT = fft2(this.(fieldName)) .* exp(1i .* this.pShift);
            this.(fieldName) = ifft2(shiftedHoloFT);
        end
        
        % Calculates the difference (i.e. error) in the phase across the
        % hp.pmWindow between two frames.  Useful as a check to see if 
        % phase error estimates are reasonable.
        function [pErr, aErr] = calcPhaseErr( this, base, fieldName )

            
            base = base.(fieldName);
            tmplt = this.(fieldName);
            % Apply hp.xcWindow
            % If no hp.xcWindow given, use default xcEdge instead
            if( isempty( this.hp.pmWindow ) )
                pmWindowTemp = [ this.xcEdge+1, size(this.reg) - this.xcEdge ];
            else
                pmWindowTemp = this.hp.pmWindow;
            end

            % Generally we're operating on reg, but the hp.xcWindow should be
            % shrunk appropriately to deal with the difference in size
            % between this.sideband and this.reg
            xcRescale_x = this.hp.holoSize(1)./this.hp.reconSize(1);
            xcRescale_y = this.hp.holoSize(2)./this.hp.reconSize(2);
            % First rescale
            reconpmWindow = [ ceil(pmWindowTemp(1)./xcRescale_x), ceil(pmWindowTemp(2)./xcRescale_y), ...
                floor(pmWindowTemp(3)./xcRescale_x), floor(pmWindowTemp(4)./xcRescale_y) ];
            
            if( reconpmWindow(1) <= 0 )
                disp( ['reconpmWindow(1) clipping: ', num2str(reconpmWindow(1)) ] );
                reconpmWindow(1) = 1;
            end
            if( reconpmWindow(2) <= 0 )
                disp( ['reconpmWindow(2) clipping: ', num2str(reconpmWindow(2)) ] );
                reconpmWindow(2) = 1;
            end
            if( reconpmWindow(3) > size( tmplt, 2 ) )
                disp( ['reconpmWindow(3) clipping: ', num2str(reconpmWindow(3)) ] );
                reconpmWindow(3) = size( tmplt, 2 );
            end
            if( reconpmWindow(4) > size( tmplt, 1 ) )
                disp( [ 'reconpmWindow(4) clipping: ', num2str(reconpmWindow(4)) ] );
                reconpmWindow(4) = size( tmplt, 1 );
            end
            
            % Calculate difference between consequetive cumReg frames
            % No difference by normalizing out intensity
            
            tmplt2 = tmplt( reconpmWindow(2):reconpmWindow(4), reconpmWindow(1):reconpmWindow(3) );
            base2 = base( reconpmWindow(2):reconpmWindow(4), reconpmWindow(1):reconpmWindow(3) );
            
            pDiff = tmplt2./ base2;
            % Ok, let's try calculating the rms phase error
            pErr = (1./sqrt(2)).*sqrt( mean2( angle(pDiff).^2 ) );
            
            aErr = sqrt( mean2( abs(pDiff).^2 ) );
        end
        
        % Calculate the dose inside the x-correlation window
        function [mDose] = calcDose( this )
        % Note that the amplitude is A_r^2 + A_o^2, so for radiation
        % damage purposes only half the dose inside the fringes counts.
        % Hence the mulitple of 0.5 in front.
            
            if( ~isempty(this.hp.xcWindow ) )
                % hp.xcWindow = [] % The window to x-correlate onto, [x1 y1 x2 y2]
                this.dose = 0.5.*mean2( this.holo( this.hp.xcWindow(2):this.hp.xcWindow(4), this.hp.xcWindow(1):this.hp.xcWindow(3) ) );
            else
                disp( 'No hp.xcWindow provided, dose likely inaccurate' );
                % xcEdge = [32 32]; 
                this.dose = 0.5.*mean2( this.holo( this.xcEdge(1):end-this.xcEdge(1), this.xcEdge(2):end-this.xcEdge(2) ) );
            end
            mDose = this.dose;
        end
        
        % Creates a partial derivative of a phase image and takes into
        % account for phase jumps.  For alignment on pure-phase objects.
        function makePhaseDiff( this, fieldName, jumpThreshold )
            %this version does differential in both directions and averages
            %them together
            pImage = angle(this.(fieldName));
            pImageDown = circshift(pImage,1);
            pDiffDown = pImage - pImageDown;
            pPlusJumps = pDiffDown > (2*pi - jumpThreshold);
            pMinusJumps = pDiffDown < (-2*pi + jumpThreshold);
            pDiffDown = pDiffDown - 2.*pi.* pPlusJumps;
            pDiffDown = pDiffDown + 2.*pi.* pMinusJumps;
            
            pImageLeft = circshift(pImage,[0,1]);
            pDiffLeft = pImage - pImageLeft;
            pPlusJumps = pDiffLeft > (2*pi - jumpThreshold);
            pMinusJumps = pDiffLeft < (-2*pi + jumpThreshold);
            pDiffLeft = pDiffLeft - 2.*pi.* pPlusJumps;
            pDiffLeft = pDiffLeft + 2.*pi.* pMinusJumps;
            
            % angle taken again to test
            this.phaseDiff = (pDiffDown + pDiffLeft)/2;
            
        end
        
        % 2-D histogramming. Depricated.
        function [dmap_out, xlim, ylim] = densityMap( this, fieldNameX, fieldNameY, bin, fScale, limits )
            % bin is size of the density map (defaults to [64 64]
            % limits is the limits method, default is histClim, 'minmax' is
            % also available.
            % fScale is a 2-element constant (X,Y) to divide each field by before
            % processing (or subtract for phase) so that reference images
            % may be applied.
        
            if( nargin >= 4 )
                binx = bin(1);
                biny = bin(2);
            else
                binx = 64;
                biny = 64;
            end
            
            % Set fScale appropriately 
            if( nargin < 5 )
                if( strcmp( fieldNameX, 'regunphase') == 1 || strcmp( fieldNameX, 'unphase') == 1 ) 
                    fScale(1) = 0;
                else
                    fScale(1) = 1;
                end
                if( strcmp( fieldNameY, 'regunphase') == 1 || strcmp( fieldNameY, 'unphase') == 1 ) 
                    fScale(2) = 0;
                else
                    fScale(2) = 1;
                end
            end

            % Grab the appropriate fields
            if( strcmp( fieldNameX, 'regunphase') == 1 )
                fieldX = FouUnwrap( angle(this.regside) );
                fieldX = fieldX - fScale(1);
            elseif( strcmp( fieldNameX, 'unphase') == 1 )
                fieldX = FouUnwrap( angle(this.side) );
                fieldX = fieldX - fScale(1);
            elseif( strcmp( fieldNameX, 'regamp') == 1 )
                fieldX = sqrt( this.regcenter );
                fieldX = fieldX./fScale(1);
            elseif( strcmp( fieldNameX, 'amp') == 1 )
                fieldX = sqrt( this.center );
                fieldX = fieldX./fScale(1);
            else
                fieldX = this.(fieldNameX);
                fieldX = fieldX./fScale(1);
            end

            if( strcmp( fieldNameY, 'regunphase') == 1 )
                fieldY = FouUnwrap( angle(this.regside) );
                fieldY = fieldY - fScale(2);
            elseif( strcmp( fieldNameY, 'unphase') == 1 )
                fieldY = FouUnwrap( angle(this.side) );
                fieldY = fieldY - fScale(2);
            elseif( strcmp( fieldNameY, 'regamp') == 1 )
                fieldY = sqrt( this.regcenter );
                fieldY = fieldY./fScale(2);
            elseif( strcmp( fieldNameY, 'amp') == 1 )
                fieldY = sqrt( this.center );
                fieldY = fieldY./fScale(2);
            else
                fieldY = this.(fieldNameY);
                fieldY = fieldY./fScale(2);
            end

            if( numel(fieldX) ~= numel(fieldY) )
                error( 'HoloClass.densityMap: Error, fields must be the same size' );
            end

            % Vectorize the fields
            fieldX = reshape( fieldX, numel( fieldX ), 1 );
            fieldY = reshape( fieldY, numel( fieldY ), 1 );

            if( nargin >= 6 )
                if( strcmp( limits, 'minmax' ) )
                    xlim(1) = min( fieldX );
                    xlim(2) = max( fieldX );
                    ylim(1) = min( fieldY );
                    ylim(2) = max( fieldY );
                % elseif( strcmp( limits, 'clim' ) )
                else
                    xlim = histClim( fieldX, 3 );
                    ylim = histClim( fieldY, 3 );
                end

            else
                xlim = histClim( fieldX, 3 );
                ylim = histClim( fieldY, 3 );
            end

            xvect = linspace(xlim(1),xlim(2),binx);
            yvect = linspace(ylim(1),ylim(2),biny);
            
            dx = xvect(2) - xvect(1);
            dy = yvect(2) - yvect(1);
            
            this.dmap = zeros( [binx biny] );
            nx = numel(xvect);
            ny = numel(yvect);

            for I = 1:numel(fieldX)
                % Might want to replace with some bilinear interpolation, but this will work for
                % now
                x_pos = find( xvect >= fieldX(I), 1, 'first' );
                y_pos = find( yvect >= fieldY(I), 1, 'first' );
                if( isempty(x_pos) ) x_pos = nx; end
                if( isempty(y_pos) ) y_pos = ny; end
                x_pos = min( max( 1, x_pos ), nx );
                y_pos = min( max( 1, y_pos ), ny );
                
                % Find remainder IN TERMS OF PIXELS
                x_rem = (xvect(x_pos) - fieldX(I) ) ./ dx;
                y_rem = (yvect(y_pos) - fieldY(I) ) ./ dy;

                
                if( x_pos == nx & y_pos == ny )
                    this.dmap(y_pos,x_pos) = this.dmap(y_pos,x_pos) + 1;
                elseif x_pos == nx
                    % y_rem = rem(fieldY(I) - yvect(y_pos),1)./dy;
                    
                    this.dmap(y_pos,x_pos) = this.dmap(y_pos,x_pos) + (1-y_rem);
                    this.dmap(y_pos+1,x_pos) = this.dmap(y_pos+1,x_pos) + y_rem;
                elseif y_pos == ny
                    % x_rem = rem(fieldX(I) - xvect(x_pos),1)./dx;
                    
                    this.dmap(y_pos,x_pos) = this.dmap(y_pos,x_pos) + (1-x_rem);
                    this.dmap(y_pos,x_pos+1) = this.dmap(y_pos,x_pos+1) + x_rem;
                else
                    % x_rem = rem(xvect(x_pos) - fieldX(I),1)./dx;
                    % y_rem = rem(yvect(y_pos) - fieldY(I),1)./dy;
                    
                    % size(x_rem)
                    % size(y_rem)
                    
                    this.dmap(y_pos,x_pos) = this.dmap(y_pos,x_pos) + (1-x_rem).*(1-y_rem);
                    this.dmap(y_pos+1,x_pos) = this.dmap(y_pos+1,x_pos) + (1-x_rem).*y_rem;
                    this.dmap(y_pos,x_pos+1) = this.dmap(y_pos,x_pos+1) + x_rem.*(1-y_rem);
                    this.dmap(y_pos+1,x_pos+1) = this.dmap(y_pos+1,x_pos+1) + x_rem.*y_rem;
                end
            end
            
            % figure; movegui;
            % imagesc( xlim, ylim, this.dmap );
            % axis square;
            dmap_out = this.dmap;
        end
        
